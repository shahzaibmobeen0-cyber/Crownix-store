function reviews(seed: string, count: number): ProductReview[] {
  const names = [
    "Ahmed R.", "Fatima K.", "Bilal S.", "Ayesha M.", "Usman T.",
    "Hira N.", "Omar F.", "Zainab A.", "Hamza Q.", "Sana J.",
  ];
  const comments = [
    "Feels far more premium than the price suggests. Strap is genuinely comfortable.",
    "Dial finishing catches the light beautifully. Arrived well packaged.",
    "Exactly as pictured. Runs a few seconds fast per week, easily adjusted.",
    "Bought this for my father — he hasn't taken it off since.",
    "Clasp feels solid, no wobble. Would buy another colourway.",
    "Great weight on the wrist, doesn't feel hollow like other pieces I've owned.",
    "Customer support on WhatsApp was fast and helpful with sizing.",
  ];
  const out: ProductReview[] = [];
  for (let i = 0; i < count; i++) {
    out.push({
      id: `${seed}-r${i}`,
      author: names[(i * 3 + seed.length) % names.length]!,
      rating: [5, 5, 4, 5, 4, 5, 3][i % 7]!,
      comment: comments[(i * 2 + seed.length) % comments.length]!,
      date: new Date(Date.now() - i * 1000 * 60 * 60 * 24 * (3 + i)).toISOString(),
      verified: i % 3 !== 0,
    });
  }
  return out;
}

